public class Point
{
    private int x;
    private int y;

    public void setPosition(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getPositionX()
    {
        return this.x;
    }

    public int getPostionY()
    {
        return this.y;
    }

}
